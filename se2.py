entrada = input(" Você quer [entrar ou sair ?]")
entrada = entrada.lower()

if entrada == 'entrar' :
    print(" Seja bem vindo, entrou no sistema! ")
elif entrada == 'sair' :
    print(' Você saiu do sistema. ')
else:
    print(' Você não digitou nem entrar e nem sair ')




